/*
 Crear un for que vaya del 1 al 10, y recurriendo al break hacer que se detenga en 5.
 */
package ejercicio19a_prueba;

/**
 *
 * @author Aritz y Juan David
 */
public class Ejercicio19A_prueba {

    public static void main(String[] args) {
         for (int i=1; i<=10; i++){ //Creamos el bucle para que vaya del 1 al 10.
             System.out.println(i);
              if (i==5){ //En el momento que sea igual a 5 hacemos uso de break.
                  break;
                  }
                   
               }
       }
}
    
